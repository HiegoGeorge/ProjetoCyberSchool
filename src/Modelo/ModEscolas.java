public class ModEscolas {

    
    
    private int codigoesc;
    private String nomeesc;
    private String enderecoesc;
    private String cnpjesc;
    private String pesquisa;
    
    
        
    
    /**
     * @return the codigoesc
     */
    public int getCodigoesc() {
        return codigoesc;
    }

    /**
     * @param codigoesc the codigoesc to set
     */
    public void setCodigoesc(int codigoesc) {
        this.codigoesc = codigoesc;
    }

    /**
     * @return the nomeesc
     */
    public String getNomeesc() {
        return nomeesc;
    }

    /**
     * @param nomeesc the nomeesc to set
     */
    public void setNomeesc(String nomeesc) {
        this.nomeesc = nomeesc;
    }

    /**
     * @return the enderecoesc
     */
    public String getEnderecoesc() {
        return enderecoesc;
    }

    /**
     * @param enderecoesc the enderecoesc to set
     */
    public void setEnderecoesc(String enderecoesc) {
        this.enderecoesc = enderecoesc;
    }

    /**
     * @return the cnpjesc
     */
    public String getCnpjesc() {
        return cnpjesc;
    }

    /**
     * @param cnpjesc the cnpjesc to set
     */
    public void setCnpjesc(String cnpjesc) {
        this.cnpjesc = cnpjesc;
    }

    /**
     * @return the pesquisa
     */
    public String getPesquisa() {
        return pesquisa;
    }

    /**
     * @param pesquisa the pesquisa to set
     */
    public void setPesquisa(String pesquisa) {
        this.pesquisa = pesquisa;
    }

    
    

    
  

    
    
}
